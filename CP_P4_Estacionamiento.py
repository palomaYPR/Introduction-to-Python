# Elaborar un programa que calcule el pago que un auto
# debe de realizar de acuerdo a las horas que estuvo estacionado.

hour = input('Horas: ')
h = float(hour)
rateHour = input('Pago por hora: ')
ph = float(rateHour)

pago = h * ph
print(f'A pagar: {pago}')

